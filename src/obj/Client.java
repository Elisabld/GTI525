package obj;

public class Client {

	private String prenom;
	private String nom;
	private String noTel;
	private Adresse adresseResidence;
	private Adresse adresseLivraison;
	
	/**
	 * @return the prenom
	 */
	public String getPrenom() {
		return prenom;
	}
	/**
	 * @param prenom the prenom to set
	 */
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}
	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	/**
	 * @return the noTel
	 */
	public String getNoTel() {
		return noTel;
	}
	/**
	 * @param noTel the noTel to set
	 */
	public void setNoTel(String noTel) {
		this.noTel = noTel;
	}
	/**
	 * @return the adresseResidence
	 */
	public Adresse getAdresseResidence() {
		return adresseResidence;
	}
	/**
	 * @param adresseResidence the adresseResidence to set
	 */
	public void setAdresseResidence(Adresse adresseResidence) {
		this.adresseResidence = adresseResidence;
	}
	/**
	 * @return the adresseLivraison
	 */
	public Adresse getAdresseLivraison() {
		return adresseLivraison;
	}
	/**
	 * @param adresseLivraison the adresseLivraison to set
	 */
	public void setAdresseLivraison(Adresse adresseLivraison) {
		this.adresseLivraison = adresseLivraison;
	}
	
}
