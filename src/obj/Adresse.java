package obj;

public class Adresse {
	
	private int noCivique;
	private String noAppart;
	private String rue;
	private String ville;
	private String province;
	private String codePostal;
	
	/**
	 * @return the noCivique
	 */
	public int getNoCivique() {
		return noCivique;
	}
	/**
	 * @param noCivique the noCivique to set
	 */
	public void setNoCivique(int noCivique) {
		this.noCivique = noCivique;
	}
	/**
	 * @return the noAppart
	 */
	public String getNoAppart() {
		return noAppart;
	}
	/**
	 * @param noAppart the noAppart to set
	 */
	public void setNoAppart(String noAppart) {
		this.noAppart = noAppart;
	}
	/**
	 * @return the rue
	 */
	public String getRue() {
		return rue;
	}
	/**
	 * @param rue the rue to set
	 */
	public void setRue(String rue) {
		this.rue = rue;
	}
	/**
	 * @return the ville
	 */
	public String getVille() {
		return ville;
	}
	/**
	 * @param ville the ville to set
	 */
	public void setVille(String ville) {
		this.ville = ville;
	}
	/**
	 * @return the province
	 */
	public String getProvince() {
		return province;
	}
	/**
	 * @param province the province to set
	 */
	public void setProvince(String province) {
		this.province = province;
	}
	/**
	 * @return the codePostal
	 */
	public String getCodePostal() {
		return codePostal;
	}
	/**
	 * @param codePostal the codePostal to set
	 */
	public void setCodePostal(String codePostal) {
		this.codePostal = codePostal;
	}
}
